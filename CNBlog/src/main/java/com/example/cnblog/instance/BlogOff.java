package com.example.cnblog.instance;

public class BlogOff {
	
	private String summary;
	private String title;
	private String bloger;
	private String blogerUrl;
	private String blogLink;
	private long storeTime;
	private String updateTime;
	private int blogId;
	private String blogApp;
	public String getBlogApp() {
		return blogApp;
	}
	public void setBlogApp(String blogApp) {
		this.blogApp = blogApp;
	}
	public String getSummary() {
		return summary;
	}
	public void setSummary(String summary) {
		this.summary = summary;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getBloger() {
		return bloger;
	}
	public void setBloger(String bloger) {
		this.bloger = bloger;
	}
	public String getBlogerUrl() {
		return blogerUrl;
	}
	public void setBlogerUrl(String blogerUrl) {
		this.blogerUrl = blogerUrl;
	}
	public String getBlogLink() {
		return blogLink;
	}
	public void setBlogLink(String blogLink) {
		this.blogLink = blogLink;
	}
	public long getStoreTime() {
		return storeTime;
	}
	public void setStoreTime(long storeTime) {
		this.storeTime = storeTime;
	}
	public String getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}
	public int getBlogId() {
		return blogId;
	}
	public void setBlogId(int blogId) {
		this.blogId = blogId;
	}
	
}
